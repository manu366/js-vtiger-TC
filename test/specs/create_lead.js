describe("Lead",async()=>{
    it("create lead",async()=>{
        await browser.url('http://localhost:8888/')
        await browser.maximizeWindow()
        await expect(browser).toHaveUrlContaining("localhost:8888")
        const username=await browser.$("//input[@name='user_name']")
        username.setValue("admin")
        const password=await browser.$("//input[@name='user_password']")
        password.setValue("admin")
        const loginBTN=await browser.$("//input[@id='submitButton']")
        loginBTN.click()
        await expect(browser).toHaveUrlContaining("vtiger")
        const lead=await browser.$("//a[.='Leads']")
        lead.click()
        await expect(browser).toHaveUrlContaining("Leads&action")
        const createlead=await browser.$("//img[@title='Create Lead...']")
        createlead.click()
        await expect(browser).toHaveUrlContaining("EditView&return_action")
        const lastname=await browser.$("//input[@name='lastname']")
        lastname.setValue("reddy")
        const company=await browser.$("//input[@name='company']")
        company.setValue("TYSS")
        const leadsource=await browser.$("//select[@name='leadsource']")
        leadsource.selectByVisibleText("Employee")
        const industry=await browser.$("//select[@name='industry']")
        industry.selectByVisibleText("Banking")
        const group=await browser.$("//input[@value='T']")
        group.click()
        const groupid=await browser.$("//select[@name='assigned_group_id']")
        groupid.selectByVisibleText("Team Selling")
        const saveBTN=await browser.$("//input[@title='Save [Alt+S]']")
        saveBTN.click()
        await expect(browser).toHaveUrlContaining("Leads&record")
        const administretor=await browser.$("//img[@src='themes/softed/images/user.PNG']")
        administretor.moveTo()
        const singout=await browser.$("//a[.='Sign Out']")
        singout.click()
    })
})