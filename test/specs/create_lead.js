describe("Lead",async()=>{
    it("create lead",async()=>{
        await browser.url('http://localhost:8888/')
        await browser.maximizeWindow()
        await expect(browser).toHaveUrlContaining("http://localhost:8888/")
        await browser.$("//input[@name='user_name']").setValue("admin")
        await browser.$("//input[@name='user_password']").setValue("admin")
        await browser.$("//input[@id='submitButton']").click()
        await expect(browser).toHaveUrlContaining("Home")
        await browser.$("//a[.='Leads']").click()
        await expect(browser).toHaveUrlContaining("Leads&action")
        await browser.$("//img[@title='Create Lead...']").click()
        await expect(browser).toHaveUrlContaining("EditView&return_action")
        await browser.$("//input[@name='lastname']").setValue("reddy")
        await browser.$("//input[@name='company']").setValue("TYSS")
        await browser.$("//select[@name='leadsource']").selectByVisibleText("Employee")
        await browser.$("//select[@name='industry']").selectByVisibleText("Banking")
        await browser.$("//input[@value='T']").click()
        await browser.$("//select[@name='assigned_group_id']").selectByVisibleText("Team Selling")
        await browser.$("//input[@title='Save [Alt+S]']").click()
        await expect(browser).toHaveUrlContaining("Leads&record")
        await browser.$("//img[@src='themes/softed/images/user.PNG']").moveTo()
        await browser.$("//a[.='Sign Out']").click()
    })
})